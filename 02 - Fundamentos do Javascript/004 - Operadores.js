//Operadores de Atribuição
let x = 10; // atribuição simples
let y = 5;
x += y; // atribuição de adição: x = x + y
x -= y; // atribuição de subtração: x = x - y
x *= y; // atribuição de multiplicação: x = x * y
x /= y; // atribuição de divisão: x = x / y

//Operadores de compração
let a = 5;
let b = 10;
let igual = a == b; // igualdade: false
let diferente = a != b; // diferença: true
let maior = a > b; // maior que: false
let menor = a < b; // menor que: true
let maiorOuIgual = a >= b; // maior ou igual a: false
let menorOuIgual = a <= b; // menor ou igual a: true

//Operadores lógicos
let a = true;
let b = false;
let resultadoE = a && b; // AND lógico: false
let resultadoOU = a || b; // OR lógico: true
let resultadoNao = !a; // NOT lógico: false

